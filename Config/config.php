<?php

$config = array();

/*
|--------------------------------------------------------------------------
| Get Modules Settings
|--------------------------------------------------------------------------
*/
$path = __DIR__."/../settings.json";
if (File::exists($path)) {
    $file = File::get($path);
    $plugin_config = json_decode($file);
    $config = (array)$plugin_config;
}

/*
|--------------------------------------------------------------------------
| Custom Modules Settings
|--------------------------------------------------------------------------
*/

$modules_settings = [];

$config = array_merge($config, $modules_settings);

return $config;